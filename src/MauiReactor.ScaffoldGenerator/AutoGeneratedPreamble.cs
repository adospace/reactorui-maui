using System.Text;

namespace MauiReactor.ScaffoldGenerator;

internal static class AutoGeneratedPreamble
{
    private static readonly string[] _warningsToDisable =
    [
        "CS1591", // Missing XML comment for publicly visible type or member
    ];

    internal static string Build(string code)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");

        foreach (var warning in _warningsToDisable)
        {
            sb.AppendLine($"#pragma warning disable {warning}");
        }

        sb.Append(code);
        return sb.ToString();
    }
}
